function parseCSV(csv) {
  const lines = csv.trim().split('\n');
  const headers = lines[0].split(',');
  const result = [];
  
  for (let i = 1; i < lines.length; i++) {
    const cols = lines[i].split(',');
    if (cols.length < headers.length) continue;
    
    // As colunas são estáticas: id, cod, cod_interno, equipamento, numero_serial, preco_custo, nf_doc, data_entrega, parceiro, obs, origem, sheet_origem
    const row = {};
    headers.forEach((h, index) => {
       row[h] = cols[index] || '';
    });
    result.push(row);
  }
  return result;
}

